//! Dioxus Web Dashboard - Compiles into the 4.2 MB binary
//!
//! Reactive web UI using Dioxus framework.
//! No separate build step, no node_modules.

use axum::{
    http::header,
    response::IntoResponse,
};

/// Serve the dashboard SPA
pub async fn serve_dashboard() -> impl IntoResponse {
    // Dioxus compiles the dashboard into static HTML/JS at build time
    // This is the embedded index.html
    (
        [(header::CONTENT_TYPE, "text/html; charset=utf-8")],
        DASHBOARD_HTML,
    )
}

/// Serve embedded assets (CSS, JS)
pub async fn serve_assets(path: axum::extract::Path<String>) -> impl IntoResponse {
    let path = path.0;
    
    match path.as_str() {
        "dashboard.css" => (
            [(header::CONTENT_TYPE, "text/css; charset=utf-8")],
            DASHBOARD_CSS,
        ).into_response(),
        "dashboard.js" => (
            [(header::CONTENT_TYPE, "application/javascript; charset=utf-8")],
            DASHBOARD_JS,
        ).into_response(),
        _ => (axum::http::StatusCode::NOT_FOUND, "Not found").into_response(),
    }
}

