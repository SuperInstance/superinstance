const DASHBOARD_JS: &str = r#"
// SuperInstance Dashboard - Minimal reactive layer
// Connects to WebSocket for real-time updates

let ws;

function connectWebSocket() {
    ws = new WebSocket(`ws://${location.host}/ws`);

    ws.onopen = () => {
        console.log('🟢 Connected to Ranch');
        requestStatus();
    };

    ws.onclose = () => {
        console.log('🔴 Disconnected from Ranch');
        setTimeout(connectWebSocket, 3000);
    };

    ws.onmessage = (event) => {
        try {
            const data = JSON.parse(event.data);
            updateDashboard(data);
        } catch (e) {
            console.log('Message:', event.data);
        }
    };
}

function requestStatus() {
    if (ws && ws.readyState === WebSocket.OPEN) {
        ws.send('status');
    }
}

function updateDashboard(data) {
    console.log('Ranch status:', data);
    if (data.day !== undefined) {
        document.getElementById('day-counter').textContent = `Day ${data.day}`;
    }
    if (data.money_saved !== undefined) {
        document.getElementById('savings').textContent = data.money_saved.toFixed(2);
    }
    if (data.cloud_calls_avoided !== undefined) {
        document.getElementById('cloud-calls').textContent = data.cloud_calls_avoided;
    }
}

// Keyboard shortcuts
document.addEventListener('keydown', (e) => {
    // Don't trigger if typing in an input
    if (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA') return;
    
    switch(e.key.toLowerCase()) {
        case 'd': triggerAction('morning-routine'); break;
        case 'n': triggerAction('night-school'); break;
        case 'b': triggerAction('create-breed'); break;
        case 'q': triggerAction('quit'); break;
    }
});

async function triggerAction(action) {
    try {
        const res = await fetch(`/api/${action}`, { method: 'POST' });
        const data = await res.json();
        console.log('Action result:', data);
        requestStatus();
    } catch (e) {
        console.error('Action failed:', e);
    }
}

// Start connection
connectWebSocket();

// Periodic status updates
setInterval(requestStatus, 5000);
"#;
